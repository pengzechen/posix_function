#include <stdio.h>



int main() {

    int number = 42;

    float pi = 3.14159;

    char letter = 'A';


    printf("Hello, World! This is a demo of the printf function.\n");

    printf("The number is: %d\n", number);

    printf("The value of pi is approximately: %.2f\n", pi);

    printf("The letter is: %c\n", letter);


    return 0;
}

